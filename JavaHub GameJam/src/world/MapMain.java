	public MapMain(int[][] map) {
		for(int y = 0; y < map.length; y++) {
			for(int x = 0; x < map[y].length; x++) {
				if(map[y][x] == 1)  new RenderTile(x * tileF.tileWidth, Main.height - tileF.tileHeight - y * tileF.tileHeight, "foreground", 0);
				if(map[y][x] == 2) new RenderTile(x * tileB.tileWidth, Main.height - tileB.tileHeight - y * tileB.tileHeight, "background", 0);
				if(map[y][x] == 3) new RenderTile(x * tileU.tileWidth, Main.height - tileU.tileHeight - y * tileU.tileHeight, "universal", 0);
				if(map[y][x] == 4) new RenderTile(x * tileU.tileWidth, Main.height - tileU.tileHeight - y * tileU.tileHeight, "finishingpost", 0);
				
				if(map[y][x] == 5) new RenderTile(x * tileU.tileWidth, Main.height - tileU.tileHeight - y * tileU.tileHeight, "pressurepad", 1);
				if(map[y][x] == 6) new EntityActivatable("universal", x, y, entityActivatable.moveableTileX, entityActivatable.moveableTileY);
			}
		}
	}
	
	public static void loadLevel(int levelID) {
		if(RenderTile.finishingPostFrontGreen == 1 && RenderTile.finishingPostBackGreen == 1) {
			if(Main.levelID != 6) {
				Main.levelID++;
				entityActivatable.resetPlayerPositions();
				Main.clock = 0;
			}else{
				Mouse.setGrabbed(false);
				GameStates.state = GameStates.state.MENU_GAME_COMPLETE;
			}
		}
		
		if(levelID == 1) new MapMain(map1);
		if(levelID == 2) new MapMain(map2);
		if(levelID == 3) new MapMain(map3);
		if(levelID == 4) new MapMain(map4);
		if(levelID == 5) new MapMain(map5);
		if(levelID == 6) new MapMain(map6);
	}
